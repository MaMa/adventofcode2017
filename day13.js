function part2(notes) {

  const ranges = parseRanges(notes).map(n => (n-1)*2)
  let pos = ranges.map(() => 0)

  function step(wall) {
    for (let i=0; i < wall.length; i++) {
      wall[i] = ranges[i] ? (wall[i]+1)%ranges[i] : -1
    }
  }

  function attempt(wall) {
    let w = [...wall]
    let pos = 0
    while (pos <= w.length) {
      if (w[pos] === 0) {
        return false
      }
      step(w)
      pos += 1
    }
    return true
  }

  let delay = 0
  do {
    if (attempt(pos)) {
      return delay
    }

    delay += 1
    step(pos)
   } while(true)
}

