class Gate {

  constructor(depth, range) {
    this.depth = depth
    this.range = range
    this.pos = 0
    this.dir = 1
  }

  get position() {
    return this.pos
  }

  at(pos) {
    return this.pos == pos
  }

  atZero() {
    return this.at(0)
  }

  severity() {
    return this.depth * this.range
  }

  step() {
    if (this.pos <= 0) {
      this.dir = 1
    } else if (this.pos >= this.range-1) {
      this.dir = -1
    }
    this.pos += this.dir
  }
}

function parseWall(input) {
  const out = []
  const lines = input.split(/\n/).map(s => s.trim().split(': ').map(Number))
  lines.map(([depth, range]) => out[depth] = new Gate(depth, range))
  return out
}

function parseRanges(input) {
  const out = []
  const lines = input.split(/\n/).map(s => s.trim().split(': ').map(Number))
  lines.map(([depth, range]) => out[depth] = range)
  return out
}

const stepWall = (wall) => wall.map(gate => gate && gate.step())

function part1(input) {
  const wall = parseWall(input)
  let severity = 0
  let len = wall.length
  let pos = 0
  while (pos <= len) {
    const gate = wall[pos]
    if (gate && gate.atZero()) {
      severity += gate.severity()
    }
    stepWall(wall)
    pos += 1
  }
  return severity
}

